/*
 * integer constant defined to identify four direction
 * North == upward in arena
 * South == downward in arena
 * West == left in arena
 * East == right in arena
 * Directions are arranged in clockwise manner for the ease of update direction
 */
public class Direction {
	public static final int NORTH = 0;
    public static final int EAST = 1;
    public static final int SOUTH = 2;
    public static final int WEST = 3;
}
